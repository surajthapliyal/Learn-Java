package StringsBook;

public class toString {

	public static void main(String[] args) {
		String s = "SURAJ"; //it is used as an object too
		System.out.println("String = "+s);
		System.out.println("String = "+s.toString());
		
		//generated same output as whenever we print object on console the toString() method
		//gets automatically called .
	}

}
