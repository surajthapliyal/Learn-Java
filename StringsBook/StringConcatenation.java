package StringsBook;

public class StringConcatenation {

	public static void main(String[] args) {
		String age = "19";
		String s = "She is "+age+" years old.";
		System.out.println("String = "+s);
		
		
		String longstr = "Hello this is a "+
						"long literal string which "+
						"declared in multiline.";
		System.out.println("Long string = "+longstr);
	}

}
